import os
import shutil

source_folder = 'problems_no_num/'

# Your target folders
target_folders = ['introductory_problems', 'sorting_and_searching', 'dynamic_programming', 'graph_algorithms', 'range_queries', 'tree_algorithms', 'mathematics', 'string_algorithms', 'geometry', 'advanced_techniques', 'additional_problems']

for folder in target_folders:
    if not os.path.exists(folder):
        os.makedirs(folder)

# Function to move the files
def move_files(file_list, target_folder):
    for file_name in file_list:
        # Add the .txt extension
        file_name_txt = file_name + '.txt'
        # Use try-except to catch any file errors
        try:
            # Copy the file
            shutil.copy(source_folder + file_name_txt, target_folder)
        except FileNotFoundError:
            print(f"The file {file_name_txt} was not found in the source directory.")
        except Exception as e:
            print(f"An error occurred while trying to copy the file {file_name_txt}: {str(e)}")


move_files(sorting_and_searching, 'sorting_and_searching')
move_files(dynamic_programming, 'dynamic_programming')
move_files(graph_algorithms, 'graph_algorithms')
move_files(range_queries, 'range_queries')
move_files(tree_algorithms, 'tree_algorithms')
move_files(mathematics, 'mathematics')
move_files(string_algorithms, 'string_algorithms')
